package com.example.nevihationapplication;

public class ItemModelAdv {
    private String nameAdv;
    private int imageAdv;

    public ItemModelAdv(String nameAdv, int imageAdv) {
        this.nameAdv = nameAdv;
        this.imageAdv = imageAdv;
    }

    public String getNameAdv() {
        return nameAdv;
    }

    public void setNameAdv(String nameAdv) {
        this.nameAdv = nameAdv;
    }

    public int getImageAdv() {
        return imageAdv;
    }

    public void setImageAdv(int imageAdv) {
        this.imageAdv = imageAdv;
    }
}
